          "bg-secondary text-secondary-foreground hover:bg-secondary/80",
        ghost:
          "hover:bg-accent hover:text-accent-foreground dark:hover:bg-accent/50",
        link: "text-primary underline-offset-4 hover:underline",
        /* CryptoHub-inspired pill variants */
        pill:
          "rounded-full bg-[var(--text-primary)] text-white font-semibold shadow-md hover:-translate-y-0.5 hover:shadow-lg active:scale-[0.98] transition-all duration-300",
        "pill-sage":
          "rounded-full bg-sage text-[var(--text-primary)] font-semibold shadow-md hover:scale-[1.02] hover:shadow-[0_0_20px_rgba(207,225,185,0.3)] active:scale-[0.98] transition-all duration-300",
