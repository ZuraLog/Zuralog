"""
Zuralog Cloud Brain — Reports API.

Endpoints:
  GET /api/v1/reports                         — List saved reports for the user.
  GET /api/v1/reports/generate                — Generate a report on-demand.
  GET /api/v1/reports/{report_id}             — Retrieve a specific saved report.

All endpoints are auth-guarded via ``get_authenticated_user_id``.

Note: The ``reports`` table migration (d4e5f6a7b8c9) must be applied for
the list/get endpoints to function.  Until the migration runs, those
endpoints return an empty result set rather than 500 — graceful degradation
is handled via try/except around DB queries.
"""

import logging
import uuid
from datetime import date, datetime, timezone
from typing import Any

from fastapi import APIRouter, Depends, HTTPException, Query, status
from pydantic import BaseModel, ConfigDict
from sqlalchemy import JSON, Column, DateTime, String, select, text
from sqlalchemy.ext.asyncio import AsyncSession

from app.api.v1.deps import get_authenticated_user_id
from app.database import Base, get_db
from app.services.report_generator import ReportGenerator

logger = logging.getLogger(__name__)

router = APIRouter(prefix="/reports", tags=["reports"])


# ---------------------------------------------------------------------------
# Inline ORM model (avoids importing the not-yet-migrated model from models/)
# ---------------------------------------------------------------------------


class Report(Base):
    """Saved health report row.

    Reports are generated by ``ReportGenerator`` and persisted here so
    the client can retrieve them later without re-computation.

    Attributes:
        id: UUID primary key (auto-generated).
        user_id: Owner's user ID.
        type: Report period type — ``"weekly"`` or ``"monthly"``.
        period_start: ISO date string (YYYY-MM-DD) for the period start.
        period_end: ISO date string (YYYY-MM-DD) for the period end.
        data: Full report payload as JSON.
        created_at: Server-side creation timestamp (UTC).
    """

    __tablename__ = "reports"

    id = Column(String, primary_key=True, default=lambda: str(uuid.uuid4()))
    user_id = Column(String, index=True, nullable=False)
    type = Column(String, nullable=False)
    period_start = Column(String, nullable=False)
    period_end = Column(String, nullable=False)
    data = Column(JSON, nullable=False, default=dict)
    created_at = Column(
        DateTime(timezone=True),
        nullable=False,
        server_default=text("now()"),
    )


# ---------------------------------------------------------------------------
# Pydantic schemas
# ---------------------------------------------------------------------------


class ReportSummaryResponse(BaseModel):
    """Brief report metadata returned in list responses.

    Attributes:
        id: Report UUID.
        type: ``"weekly"`` or ``"monthly"``.
        period_start: ISO date string.
        period_end: ISO date string.
        created_at: ISO-8601 creation timestamp.
    """

    id: str
    type: str
    period_start: str
    period_end: str
    created_at: str

    model_config = ConfigDict(from_attributes=True)


class ReportDetailResponse(ReportSummaryResponse):
    """Full report including the generated data payload.

    Extends ``ReportSummaryResponse`` with the ``data`` field.

    Attributes:
        data: Full report payload dict.
    """

    data: dict[str, Any]


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _dt_str(value: Any) -> str:
    """Return an ISO-8601 string for a datetime value.

    Args:
        value: A ``datetime`` instance or string.

    Returns:
        ISO-8601 string, or empty string if conversion fails.
    """
    if value is None:
        return ""
    if isinstance(value, datetime):
        return value.isoformat()
    return str(value)


def _report_to_summary(row: Report) -> ReportSummaryResponse:
    """Convert a ``Report`` ORM row to a ``ReportSummaryResponse``.

    Args:
        row: The ORM instance to serialise.

    Returns:
        A ``ReportSummaryResponse``.
    """
    return ReportSummaryResponse(
        id=row.id,
        type=row.type,
        period_start=row.period_start,
        period_end=row.period_end,
        created_at=_dt_str(row.created_at),
    )


def _report_to_detail(row: Report) -> ReportDetailResponse:
    """Convert a ``Report`` ORM row to a ``ReportDetailResponse``.

    Args:
        row: The ORM instance to serialise.

    Returns:
        A ``ReportDetailResponse`` with the full data payload.
    """
    return ReportDetailResponse(
        id=row.id,
        type=row.type,
        period_start=row.period_start,
        period_end=row.period_end,
        created_at=_dt_str(row.created_at),
        data=row.data or {},
    )


